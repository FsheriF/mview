use strict;

###########################################################################
package Bio::Util::Regexp;

use Exporter;

use vars qw(@ISA @EXPORT);

@ISA = qw(Exporter);

@EXPORT = qw(
    $RX_Uint
    $RX_Sint
    $RX_Ureal
    $RX_Sreal
    );

use vars qw(
    $RX_Uint
    $RX_Sint
    $RX_Ureal
    $RX_Sreal
    );

###########################################################################
#integers
###########################################################################

#unsigned
$RX_Uint  = '\+?\d+';

#signed
$RX_Sint  = '[+-]?\d+';

###########################################################################
#floats
###########################################################################

#unsigned
$RX_Ureal = '\+?(?:\d+\.\d+|\d+\.|\d+|\.\d+)?(?:[eE][+-]?\d+)?';

#signed
$RX_Sreal = '[+-]?(?:\d+\.\d+|\d+\.|\d+|\.\d+)?(?:[eE][+-]?\d+)?';

###########################################################################
1;
